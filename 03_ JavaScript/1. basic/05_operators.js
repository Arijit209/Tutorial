//==========================Arithmatic Operators================================//

let a = 10
let b = 5

// 1. Addition
console.log('Addition : ', a+b)
// 2. Substaction
console.log('Substaction : ', a-b)
// 3. Multiplication
console.log('Multiplication : ', a*b)
// 4. Division
console.log('Division : ', a/b)
// 5. Modulus
console.log('Modulus : ', a%b)
// 6. Exponent
console.log('Exponent : ', a**b)
// 7. Increment
console.log('Increment : ', ++a)
// 8. Decrement
console.log('Decrement : ', --b)

//==========================Logical Operators===================================//
let x = true
let y = false

// 1. AND
console.log('Logical AND : ', x && y)
// 2. OR
console.log('Logical OR : ', x || y)
// 3. NOT
console.log('Logical NOT : ', !x)

//==========================Comparision Operators================================//
let p = 10
let q = '10'

// 1. Equal to
console.log('Equal to : ', p == q)
// 2. Strict Equal to
console.log('Strict Equal to : ', p === q)
// 3. Not Equal to
console.log('Not Equal to : ', p != q)
// 4. Not Strict Equal to
console.log('Not Strict Equal to : ', p !== q)
// 5. Greater than
console.log('Greater than : ', p > q)
// 6. Less than
console.log('Less than : ', p < q)
// 7. Greater than equal to
console.log('Greater than equal to : ', p >= q)
// 8. Less than equal to
console.log('Less than equal to : ', p <= q)

//==========================Assignment Operators================================//
let m = 10

// 1. Assign
console.log('Assign : ', m = 20)
// 2. Add & Assign
console.log('Add & Assign : ', m += 20)
// 3. Subtract & Assign
console.log('Subtract & Assign : ', m -= 20)
// 4. Multiply & Assign
console.log('Multiply & Assign : ', m *= 20)
// 5. Divide & Assign
console.log('Divide & Assign : ', m /= 20)
// 6. Modulus & Assign
console.log('Modulus & Assign : ', m %= 20)
// 7. Exponential & Assign
console.log('Exponential & Assign : ', m **= 20)