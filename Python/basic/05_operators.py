#########################################Arithmetic Operators###########################################
a = 10
b = 5

# Addition
print('Addition:', a + b)

# Subtraction
print('Subtraction:', a - b)

# Multiplication
print('Multiplication:', a * b)

# Division
print('Division:', a / b)

# Flow Division (This is for python)
print('Flow Division:', a // b)

# Modulus
print('Modulus:', a % b)

# Exponent
print('Exponent:', a ** b)

#########################################Assignment Operators###########################################

# Assign
a = 20
print(a)

# Compound Assignment Operation
a += 20
print(a)

a -= 20
print(a)

a *= 20
print(a)

a /= 20
print(a)

a //= 20
print(a)

a **= 20
print(a)

a %= 20
print(a)

#########################################Comparision Operators###########################################
a = 50
b = 45

# Equal To
print(a == b)

# Not Equal Than
print(a != b)

# Greater Than
print(a > b)

# Greater Than Equal To
print(a >= b)

# Less Than
print(a < b)

# Less Than Equal To
print(a <= b)

# ASCII Value
print(ord("A"))
print("A" > "B") #check with ASCII value

#########################################Logical Operators###########################################
a = 50
b = 60

# AND 
print(a==b and a > b)

# OR 
print(a==b or a > b)

# NOT 
print(not a > b)