#######################################variable data type###########################################
'''
1. Number
    * integer -> int
    * floating number -> float, double
    * complex -> complex
2. String
    * string -> string
3. Boolean
    * boolean -> bool
'''
##############################################Number####################################################
# Integer
myInt = 10
print('Integer', type(myInt))

# Float
myFloat = 3.14
print('Float', type(myFloat))

# Complex
myComplex = 3j
print('Complex', type(myComplex))

###########################################String##############################################
# String
myString = 'Hello world, 112'
print('String', type(myString))

#############################################Boolean#########################################
# Boolean
myBool = True
print('Boolean', type(myBool))

