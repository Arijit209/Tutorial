//=================================================Primitive Data Type============================================//
/*
    string
    number
    boolean
    null
    undefined
    bigint
    symbol
*/

// String
let name = 'Arijit Dutta'
console.log(name)
console.log(typeof name)

// Number
let num = 10
console.log(num)
console.log(typeof num)

// Boolean
let bool = true
console.log(bool)
console.log(typeof bool)

// Null
let empty = null
console.log(empty)
console.log(typeof empty) // object

// Undefined
let blank
console.log(blank)
console.log(typeof blank)

// Big Integer
let big = 123456n
console.log(big)
console.log(typeof big)

// Symbol
let symb = Symbol('unique')
console.log(symb)
console.log(typeof symb)


